package com.doni.genbe.model.dto;

public class PendidikanDto {
	private String jenjang;
	private String institusi;
	private String masuk;
	private String lulus;
	private Integer kodePerson;
	
	public String getJenjang() {
		return jenjang;
	}
	public void setJenjang(String jenjang) {
		this.jenjang = jenjang;
	}
	public String getInstitusi() {
		return institusi;
	}
	public void setInstitusi(String institusi) {
		this.institusi = institusi;
	}
	public String getMasuk() {
		return masuk;
	}
	public void setMasuk(String masuk) {
		this.masuk = masuk;
	}
	public String getLulus() {
		return lulus;
	}
	public void setLulus(String lulus) {
		this.lulus = lulus;
	}
	public Integer getKodePerson() {
		return kodePerson;
	}
	public void setKodePerson(Integer kodePerson) {
		this.kodePerson = kodePerson;
	}
	
	
}
